# adding another variable & changing the value of a variable in the dict - so dict are mutable
dict = {
    "name" : "Nibedita",
    "subjects" : ["Python", "C", "Java"],
    "topics" : ("dict", "set"),
    "age" : 35,
    "is_adult" : True,
    "marks": 94.4
}
dict["name"] = "C Nibedita Swain"
dict["gender"] = "Female"
print(dict)


