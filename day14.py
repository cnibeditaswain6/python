# write a program to perform basic arithmetic operations on two numbers.

a = int(input("Enter First Number : "))
b = int(input("Enter Second Number : "))

print("Sum = ", a + b)
print("Product = ", a * b)
print("Difference = ", a - b)
print("Quotient = ", a / b)
print("Remainder = ", a % b)
print("Square = ", a ** b)